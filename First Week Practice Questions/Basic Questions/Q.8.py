# Python program to generate a random value between two integers (inclusive)
# and a random multiple of 7 between 0 and 70.

import random

print("Random value between two integers:")
print(random.randint(10, 85))
print(random.randint(-12, 24))
print("Random multiple of 7 between 0 and 70:")
print(random.randint(0, 10) * 7)
